package numcol

import "sort"

var cols = []uint64{
	10,
	100,
	1000,
	10000,
	100000,
	1000000,
	10000000,
	100000000,
	1000000000,
	10000000000,
	100000000000,
	1000000000000,
	10000000000000,
	100000000000000,
	1000000000000000,
	10000000000000000,
	100000000000000000,
	1000000000000000000,
	10000000000000000000,
}

func NumCol(n uint64) int {
	y := sort.Search(len(cols), func(x int) bool {
		return n < cols[x]
	})
	return y + 1
}