package numcol

import (
	"math"
	"testing"
)

func TestNumCol(t *testing.T) {
	for i, tc := range []struct {
		u64 uint64
		col int
	}{
		{0, 1},
		{1, 1},
		{2, 1},
		{9, 1},
		{10, 2},
		{11, 2},
		{12, 2},
		{99, 2},
		{100, 3},
		{101, 3},
		{102, 3},
		{999, 3},
		{1000, 4},
		{1001, 4},
		{9999, 4},
		{10000, 5},
		{9999999999999999, 16},
		{10000000000000000, 17},
		{99999999999999999, 17},
		{100000000000000000, 18},
		{999999999999999999, 18},
		{1000000000000000000, 19},
		{9999999999999999999, 19},
		{10000000000000000000, 20},
		{math.MaxUint64, 20},
	} {
		act := NumCol(tc.u64)
		if act != tc.col {
			t.Fatalf("NumCol failed #%d: u64=%d want=%d got=%d\n", i, tc.u64, tc.col, act)
		}
	}
}